constexpr const HTMLEntity* firstentry(char cc)
{
    if(cc >= 'A' && cc <= 'Z')
        return uppercasetable[cc - 'A'];
    if(cc >= 'a' && cc <= 'z')
        return lowercasetable[cc - 'a'];
    return nullptr;
}

constexpr const HTMLEntity* lastentry(char cc)
{
    if(cc >= 'A' && cc <= 'Z')
        return uppercasetable[cc - 'A' + 1] - 1;
    if(cc >= 'a' && cc <= 'z')
        return lowercasetable[cc - 'a' + 1] - 1;
    return nullptr;
}

constexpr const HTMLEntity* halfway(const HTMLEntity* left, const HTMLEntity* right)
{
    return &left[(right - left) / 2];
}

constexpr int compare(const HTMLEntity* entry, size_t offset, char cc)
{
    if(offset >= entry->name.length())
        return -1;

    auto nextCharacter = entry->name[offset];
    if(nextCharacter == cc)
        return 0;
    return nextCharacter < cc ? -1 : 1;
}

constexpr const HTMLEntity* lowerentry(const HTMLEntity* left, const HTMLEntity* right, size_t offset, char cc)
{
    if(left == right)
        return left;
    auto relation = compare(left, offset, cc);
    if(relation == 0)
        return left;
    if(relation > 0)
        return right;
    while(left + 1 < right) {
        auto probe = halfway(left, right);
        auto relation = compare(probe, offset, cc);
        if(relation < 0)
            left = probe;
        else
            right = probe;
    }

    assert(left + 1 == right);
    return right;
}

constexpr const HTMLEntity* upperentry(const HTMLEntity* left, const HTMLEntity* right, size_t offset, char cc)
{
    if(left == right)
        return right;
    auto relation = compare(right, offset, cc);
    if(relation == 0)
        return right;
    if(relation < 0)
        return left;
    while(left + 1 < right) {
        auto probe = halfway(left, right);
        auto relation = compare(probe, offset, cc);
        if(relation > 0)
            right = probe;
        else
            left = probe;
    }

    assert(left + 1 == right);
    return left;
}

bool HTMLEntitySearch::advance(char cc)
{
    if(m_offset == 0) {
        m_first = firstentry(cc);
        m_last = lastentry(cc);
        if(m_first == nullptr || m_last == nullptr) {
            return false;
        }
    } else {
        m_first = lowerentry(m_first, m_last, m_offset, cc);
        m_last = upperentry(m_first, m_last, m_offset, cc);
        if(m_first == m_last && compare(m_first, m_offset, cc) != 0) {
            return false;
        }
    }

    m_offset += 1;
    if(m_offset == m_first->name.length())
        m_lastMatch = m_first;
    return true;
}

bool HTMLEntityParser::parse()
{
    auto cc = m_input.peek();
    if(isalpha(cc))
        return handleNamed(cc);

    if(cc == '#')
        return handleNumber(m_input.advance());

    return false;
}

bool HTMLEntityParser::handleNamed(char cc)
{
    HTMLEntitySearch entitySearch;
    do {
        if(!entitySearch.advance(cc))
            break;
        cc = m_input.advance();
    } while(true);

    auto lastMatch = entitySearch.lastMatch();
    if(lastMatch && (lastMatch->name.back() == ';' || !m_inAttributeValue || !(isalpha(cc) || isdigit(cc) || cc == '='))) {
        append(lastMatch->firstValue);
        if(lastMatch->secondValue)
            append(lastMatch->secondValue);
        m_input -= entitySearch.offset() - lastMatch->name.length();
        return true;
    }

    return false;
}

bool HTMLEntityParser::handleNumber(char cc)
{
    if(isdigit(cc))
        return handleDecimal(cc);

    if(cc == 'x' || cc == 'X')
        return handleMaybeHex(m_input.advance());

    return false;
}

bool HTMLEntityParser::handleDecimal(char cc)
{
    uint32_t codepoint = 0;
    do {
        codepoint = codepoint * 10 + cc - '0';
        cc = m_input.advance();
    } while(isdigit(cc));

    if(cc == ';')
        m_input.advance();
    append(codepoint);
    return true;
}

bool HTMLEntityParser::handleMaybeHex(char cc)
{
    if(isxdigit(cc))
        return handleHex(cc);

    return false;
}

bool HTMLEntityParser::handleHex(char cc)
{
    uint32_t codepoint = 0;
    do {
        codepoint = codepoint * 16 + xdigit(cc);
        cc = m_input.advance();
    } while(isxdigit(cc));

    if(cc == ';')
        m_input.advance();
    append(codepoint);
    return true;
}

void HTMLEntityParser::append(uint32_t cp)
{
    if(cp == 0 || cp >= 0x10FFFF || (cp >= 0xD800 && cp <= 0xDFFF)) {
        appendCodepoint(m_output, 0xFFFD);
    } else {
        appendCodepoint(m_output, cp);
    }
}

